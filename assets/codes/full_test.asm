NEG2: CONST -2
DEZ: CONST 10
NUM: SPACE
NUM2: SPACE 4

SECAO TEXT
load DEZ
input NUM
OUTPUT NUM
ADD NEG2
MUL DEZ
STORE NUM
DIV DEZ
STORE NUM2+2
COPY NUM, NUM2+1
OUTPUT NUM2+1
OUTPUT NUM2+2
LOAD DEZ
JMP NEGATIVE

POSITIVE:
JMPP ZERO
ADD DEZ
JMP POSITIVE

NEGATIVE: 
JMPN POSITIVE
SUB DEZ
JMP NEGATIVE

ZERO:
JMPZ FINALIZAR
SUB DEZ
JMP ZERO

FINALIZAR: STOP